/**
 * Definition for a binary tree node.
 * class TreeNode {
 *     public $val = null;
 *     public $left = null;
 *     public $right = null;
 *     function __construct($val = 0, $left = null, $right = null) {
 *         $this->val = $val;
 *         $this->left = $left;
 *         $this->right = $right;
 *     }
 * }
 */
class Solution
{

    /**
     * @param TreeNode $root
     * @return int[]
     */
    public function inorderTraversal($root)
    {
        $orders = [];
        if ($root->left) {
            $orders = array_merge($orders, $this->inorderTraversal($root->left));
            // var_dump($orders);
        }
        $orders[] = $root->val;

        if ($root->right) {
            $orders = array_merge($orders, $this->inorderTraversal($root->right));
        }

        return $orders;
    }
}
